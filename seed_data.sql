  percentile numeric(5,2) check (percentile >= 0 and percentile <= 100),
  grade int check (grade >= 1 and grade <= 9),
  target_score int check (target_score >= 0 and target_score <= 100),
  created_at timestamp with time zone default now(),
  unique(user_id, exam_year, exam_month, subject)
);

alter table if exists public.mock_exam_records enable row level security;

do $rls$
begin
  if not exists (
    select 1 from pg_policies
    where schemaname = 'public'
      and tablename  = 'mock_exam_records'
      and policyname = 'mock_exam_records_select_own'
  ) then
    execute 'create policy "mock_exam_records_select_own" on public.mock_exam_records for select using (auth.uid() = user_id)';
    execute 'create policy "mock_exam_records_insert_own" on public.mock_exam_records for insert with check (auth.uid() = user_id)';
    execute 'create policy "mock_exam_records_update_own" on public.mock_exam_records for update using (auth.uid() = user_id) with check (auth.uid() = user_id)';
    execute 'create policy "mock_exam_records_delete_own" on public.mock_exam_records for delete using (auth.uid() = user_id)';
  end if;
end $rls$;

